
public class vjezbaNo1 {
	public static void main(String[] args) {
		int end = 10;
		int counter = 0;
		System.out.println("Ispis brojeva sa for petljom");
		for(int i = 0; i <= 10; i++){
			System.out.printf("%d ", i);
			
		}
		System.out.println("Ispis brojeva sa for petljom");
		counter = 0;
		while (counter <= end){
			System.out.printf("%d ", counter ++);
		}
		
	}

}
